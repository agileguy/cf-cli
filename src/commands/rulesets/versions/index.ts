import type { Context } from "../../../types/index.js";
import { UsageError } from "../../../utils/errors.js";

import * as list from "./list.js";
import * as get from "./get.js";

const USAGE = `Usage: cf rulesets versions <command>

Commands:
  list        List versions of a ruleset
  get         Get a specific version of a ruleset

Run 'cf rulesets versions <command> --help' for more information.`;

export async function run(args: string[], ctx: Context): Promise<void> {
  const [subcommand, ...rest] = args;

  switch (subcommand) {
    case "list":
    case "ls":
      return list.run(rest, ctx);
    case "get":
    case "show":
      return get.run(rest, ctx);
    case undefined:
    case "--help":
    case "-h":
      ctx.output.raw(USAGE);
      return;
    default:
      throw new UsageError(`Unknown rulesets versions command: "${subcommand}"\n\n${USAGE}`);
  }
}
