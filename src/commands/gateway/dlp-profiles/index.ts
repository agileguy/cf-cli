import type { Context } from "../../../types/index.js";
import { UsageError } from "../../../utils/errors.js";

import * as list from "./list.js";
import * as create from "./create.js";
import * as del from "./delete.js";

const USAGE = `Usage: cf gateway dlp profiles <command>

Commands:
  list        List DLP profiles
  create      Create a custom DLP profile
  delete      Delete a custom DLP profile

Run 'cf gateway dlp profiles <command> --help' for more information.`;

export async function run(args: string[], ctx: Context): Promise<void> {
  const [subcommand, ...rest] = args;

  switch (subcommand) {
    case "list":
    case "ls":
      return list.run(rest, ctx);
    case "create":
    case "add":
      return create.run(rest, ctx);
    case "delete":
    case "rm":
    case "remove":
      return del.run(rest, ctx);
    case undefined:
    case "--help":
    case "-h":
      ctx.output.raw(USAGE);
      return;
    default:
      throw new UsageError(`Unknown gateway dlp profiles command: "${subcommand}"\n\n${USAGE}`);
  }
}
